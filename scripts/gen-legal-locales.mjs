const root = join(dirname(fileURLToPath(import.meta.url)), "..");
const legalDir = join(root, "src", "i18n", "legal");

function doc(sections) {
  return { sections };
}
function s(title, blocks, id) {
  return id ? { id, title, blocks } : { title, blocks };
}
function p(text) {
  return { p: text };
}
function list(items) {
  return { list: items };
}
function policy() {
  return { policyLinks: true };
}

/** Build a full LegalBundle from compact per-locale tuples. */
function build(L) {
  return {
    privacy: doc([
      s(L.p1t, [p(L.p1)]),
      s(L.p2t, [p(L.p2intro), list(L.p2list)], "data-protection"),
      s(L.p3t, [list(L.p3list)]),
      s(L.p4t, [p(L.p4)]),
      s(L.p5t, [p(L.p5)]),
      s(L.p6t, [p(L.p6)]),
      s(L.p7t, [p(L.p7)]),
      s(L.p8t, [p(L.p8)]),
      s(L.p9t, [p(L.p9)]),
      s(L.p10t, [p(L.p10)]),
    ]),
    terms: doc([
      s(L.t1t, [p(L.t1)]),
      s(L.t2t, [p(L.t2)]),
      s(L.t3t, [list(L.t3list)]),
      s(L.t4t, [p(L.t4a), p(L.t4b)]),
      s(L.t5t, [p(L.t5)]),
      s(L.t6t, [p(L.t6)]),
      s(L.t7t, [p(L.t7)], "disclaimers"),
      s(L.t8t, [p(L.t8)]),
      s(L.t9t, [p(L.t9)]),
      s(L.t10t, [p(L.t10)]),
    ]),
    cookies: doc([
      s(L.c1t, [p(L.c1)]),
      s(L.c2t, [list(L.c2list)]),
      s(L.c3t, [p(L.c3)]),
      s(L.c4t, [p(L.c4)]),
    ]),
    security: doc([
      s(L.s1t, [p(L.s1)]),
      s(L.s2t, [list(L.s2list)], "uploads"),
      s(L.s3t, [list(L.s3list)]),
      s(L.s4t, [p(L.s4)]),
      s(L.s5t, [p(L.s5)]),
      s(L.s6t, [p(L.s6)]),
      s(L.s7t, [policy()]),
    ]),
    copyright: doc([
      s(L.r1t, [p(L.r1)]),
      s(L.r2t, [p(L.r2)]),
      s(L.r3t, [p(L.r3intro), list(L.r3list), p(L.r3end)]),
      s(L.r4t, [p(L.r4)]),
      s(L.r5t, [p(L.r5)]),
    ]),
    acceptableUse: doc([
      s(L.a1t, [p(L.a1)]),
      s(L.a2t, [list(L.a2list)]),
      s(L.a3t, [list(L.a3list)]),
      s(L.a4t, [p(L.a4)]),
      s(L.a5t, [p(L.a5)]),
    ]),
    contact: doc([
      s(L.k1t, [p(L.k1a), p(L.k1b)]),
      s(L.k2t, [p(L.k2)]),
      s(L.k3t, [policy()]),
    ]),
  };
}

